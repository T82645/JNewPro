package patternpack;


public class Patterns_K {
				
	public static void main(String[] args) {
		for(int row=1;row<=4;row++){
			for(int col=1;col<=4;col++){
				if((row+col)>5){
				System.out.print(" ");
				}else{
				System.out.print(col+" ");
		}
	}
	System.out.println();
}
	for(int i=1;i<=3;i++){
		for(int j=1;j<=i+1;j++){
			System.out.print(j+" ");
	}
	System.out.println();
}				
}
}
